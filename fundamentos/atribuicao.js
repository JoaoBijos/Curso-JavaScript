const a = 7 // = representa uma atribuicao 

let b = 3 

b += a // atribuicao aditiva, o mesmo de b = b + a
console.log(b)

b -= 4 // mesmo que b = b - 4
console.log(b)

b *= 2 // mesmo que b = b * 2
console.log(b)

b /= 2 // mesmo que b = b / 2
console.log(b)

b %= 2 // mesmo que b = b % 2
console.log(b) // retorna 0 pois o número é par 